  <article>
    <transition-group name="fade-up" tag="ul" class="products_list">
      <ProductItem
        v-for="item in displayedItems"
        :key="item.id"
        :product="item"
        class="product_item"
      />
      <!-- 감지용 li -->
      <li v-if="hasMore" ref="observerTarget" class="observer_trigger" aria-hidden="true"></li>
    </transition-group>
